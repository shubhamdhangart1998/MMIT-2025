package com.tnsif.daytwo;

public class ForEachLoopDemo {

	public static void main(String[] args) {
		
		int a[]= {10,20,30,40,50};
		
		//for integer array
		
		for(int i: a)
		{
			System.out.println(i);
			
			System.out.println("--------Hello Friends-----------");
		}
		
		char ch[]= {'j','a','v','a'};
		for(char c:ch)
		{
			System.out.print(c);
		}
	

	}

}
